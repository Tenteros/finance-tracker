let lang = 'ru';

export function setLang(code) {
  lang = dict[code] ? code : 'ru';
}

export function getLang() {
  return lang;
}

export function t(key) {
  return dict[lang][key] || dict.ru[key] || key;
}

export function getDefaultExpenseCats() {
  const c = catDict[lang] || catDict.ru;
  return [c.food, c.transport, c.fun, c.health, c.shopping];
}

export function getDefaultIncomeCats() {
  const c = catDict[lang] || catDict.ru;
  return [c.salary, c.side, c.gift];
}

export function applyI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (el.tagName === 'INPUT') {
      el.placeholder = t(key);
    } else if (el.tagName === 'OPTION') {
      el.textContent = t(key);
    } else {
      el.textContent = t(key);
    }
  });
}
